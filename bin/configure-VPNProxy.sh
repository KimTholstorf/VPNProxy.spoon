#!/usr/bin/env bash
# configure-VPNProxy.sh
#
# Auto-detects VPN CIDR, proxy host/port, and network services, then
# writes the values into ~/.hammerspoon/init.lua.
# Run this script once per machine, or again whenever your network changes.
#
# Prerequisites:
#   - VPNProxy.spoon installed in ~/.hammerspoon/Spoons/
#   - Connected to corporate VPN (required for detection)

set -e

OUTPUT="$HOME/.hammerspoon/init.lua"

# ── Output helpers ─────────────────────────────────────────────────────────────

BOLD=$(tput bold 2>/dev/null || true)
RED=$(tput setaf 1 2>/dev/null || true)
GREEN=$(tput setaf 2 2>/dev/null || true)
YELLOW=$(tput setaf 3 2>/dev/null || true)
CYAN=$(tput setaf 6 2>/dev/null || true)
RESET=$(tput sgr0 2>/dev/null || true)

header()  { echo; echo "${BOLD}${CYAN}══ $1 ══${RESET}"; echo; }
success() { echo "  ${GREEN}✅ $1${RESET}"; }
info()    { echo "  ${CYAN}→  $1${RESET}"; }
warn()    { echo "  ${YELLOW}⚠️  $1${RESET}"; }
error()   { echo; echo "  ${RED}${BOLD}ERROR: $1${RESET}"; echo; }
ask()     { printf "  ${BOLD}$1${RESET} "; }

# ── Header ─────────────────────────────────────────────────────────────────────

echo
echo "${BOLD}${CYAN}╔══════════════════════════════════════════╗${RESET}"
echo "${BOLD}${CYAN}║     VPNProxy.spoon  Auto-Detection       ║${RESET}"
echo "${BOLD}${CYAN}╚══════════════════════════════════════════╝${RESET}"
echo
echo "  Detects VPN CIDR, proxy host/port, and network services"
echo "  and writes the values into ~/.hammerspoon/init.lua."
echo

# ── Verify VPN is connected ───────────────────────────────────────────────────

header "Detecting VPN"

VPN_ADDR=$(ifconfig | grep -A2 utun | grep "inet " | grep -v "inet6" | grep -v "127.0.0.1" | awk '{print $2}' | head -1)

if [ -z "$VPN_ADDR" ]; then
  error "No VPN tunnel detected."
  echo "  Please connect to corporate VPN and re-run this script."
  exit 1
fi

success "VPN address: $VPN_ADDR"

VPN_CIDR=$(echo "$VPN_ADDR" | awk -F. '{printf "^%s\\.%s\\.", $1, $2}')
success "Lua CIDR pattern: $VPN_CIDR"

# ── Auto-detect proxy ─────────────────────────────────────────────────────────

header "Detecting Proxy"

PAC_URL=$(scutil --proxy | grep ProxyAutoConfigURLString | awk '{print $3}')

if [ -z "$PAC_URL" ]; then
  warn "No PAC URL found — falling back to environment variables..."
  PROXY_RAW="${http_proxy:-$https_proxy}"
  PROXY_HOST=$(echo "$PROXY_RAW" | sed 's|http://||' | cut -d: -f1)
  PROXY_PORT=$(echo "$PROXY_RAW" | sed 's|http://||' | cut -d: -f2)
else
  info "PAC URL: $PAC_URL"
  PAC_CONTENT=$(curl -s "$PAC_URL")

  if [ -z "$PAC_CONTENT" ]; then
    warn "Could not fetch PAC file — falling back to environment variables..."
    PROXY_RAW="${http_proxy:-$https_proxy}"
    PROXY_HOST=$(echo "$PROXY_RAW" | sed 's|http://||' | cut -d: -f1)
    PROXY_PORT=$(echo "$PROXY_RAW" | sed 's|http://||' | cut -d: -f2)
  else
    PROXY_HOST=$(echo "$PAC_CONTENT" | grep -oE 'PROXY [a-zA-Z0-9._-]+:[0-9]+' | head -1 | awk '{print $2}' | cut -d: -f1)
    PROXY_PORT=$(echo "$PAC_CONTENT" | grep -oE 'PROXY [a-zA-Z0-9._-]+:[0-9]+' | head -1 | awk '{print $2}' | cut -d: -f2)
  fi
fi

if [ -z "$PROXY_HOST" ] || [ -z "$PROXY_PORT" ]; then
  error "Could not detect proxy address."
  echo "  Edit init.lua manually and set proxyHost and proxyPort."
  exit 1
fi

success "Proxy: $PROXY_HOST:$PROXY_PORT"

# ── Detect network services ───────────────────────────────────────────────────

header "Detecting Network Services"

EXCLUDE_PATTERN="Thunderbolt Bridge|Bluetooth"
SERVICES=$(networksetup -listallnetworkservices | tail -n +2 | grep -vE "$EXCLUDE_PATTERN")

echo "$SERVICES" | while IFS= read -r svc; do
  success "$svc"
done

NETWORK_SERVICES=$(echo "$SERVICES" | while IFS= read -r svc; do
  echo "  \"$svc\","
done)

# ── Write or update init.lua ──────────────────────────────────────────────────

header "Configuring ~/.hammerspoon/init.lua"

mkdir -p "$HOME/.hammerspoon"

if [ ! -f "$OUTPUT" ]; then
  # ── No existing init.lua — create with VPNProxy config ───────────────────
  info "No existing init.lua found — creating..."
  cat > "$OUTPUT" <<EOF
-- ~/.hammerspoon/init.lua
-- Generated by configure-VPNProxy.sh

hs.loadSpoon("VPNProxy")
spoon.VPNProxy.proxyHost = "$PROXY_HOST"
spoon.VPNProxy.proxyPort = "$PROXY_PORT"
spoon.VPNProxy.vpnCIDR = "$VPN_CIDR"
spoon.VPNProxy.networkServices = {
$NETWORK_SERVICES
}
spoon.VPNProxy:start()
EOF
  success "Created $OUTPUT"

elif ! grep -q 'hs.loadSpoon("VPNProxy")' "$OUTPUT"; then
  # ── Existing init.lua, no VPNProxy — append full block ───────────────────
  info "Existing init.lua found — no VPNProxy config detected, appending..."
  cat >> "$OUTPUT" <<EOF

-- VPNProxy spoon — added by configure-VPNProxy.sh
hs.loadSpoon("VPNProxy")
spoon.VPNProxy.proxyHost = "$PROXY_HOST"
spoon.VPNProxy.proxyPort = "$PROXY_PORT"
spoon.VPNProxy.vpnCIDR = "$VPN_CIDR"
spoon.VPNProxy.networkServices = {
$NETWORK_SERVICES
}
spoon.VPNProxy:start()
EOF
  success "Appended VPNProxy config to $OUTPUT"

else
  # ── Existing init.lua with VPNProxy — ask to update ──────────────────────
  warn "Existing VPNProxy config detected in $OUTPUT"
  echo
  echo "  Detected values:"
  echo "    proxyHost       = $PROXY_HOST"
  echo "    proxyPort       = $PROXY_PORT"
  echo "    vpnCIDR         = $VPN_CIDR"
  echo "    networkServices ="
  echo "$NETWORK_SERVICES" | while IFS= read -r svc; do
    echo "      $svc"
  done
  echo
  ask "Do you want to update the VPNProxy config with these values? [y/N]"
  read -r REPLY
  case "$REPLY" in
    y|Y)
      info "Updating VPNProxy config lines..."
      TMPFILE=$(mktemp)
      IN_SERVICES=0
      while IFS= read -r line; do
        if echo "$line" | grep -q 'spoon\.VPNProxy\.proxyHost'; then
          echo "spoon.VPNProxy.proxyHost = \"$PROXY_HOST\""
        elif echo "$line" | grep -q 'spoon\.VPNProxy\.proxyPort'; then
          echo "spoon.VPNProxy.proxyPort = \"$PROXY_PORT\""
        elif echo "$line" | grep -q 'spoon\.VPNProxy\.vpnCIDR'; then
          echo "spoon.VPNProxy.vpnCIDR = \"$VPN_CIDR\""
        elif echo "$line" | grep -q 'spoon\.VPNProxy\.networkServices'; then
          printf "spoon.VPNProxy.networkServices = {\n%s\n}" "$NETWORK_SERVICES"
          IN_SERVICES=1
        elif [ "$IN_SERVICES" -eq 1 ]; then
          if echo "$line" | grep -q '^}'; then
            IN_SERVICES=0
          fi
        else
          echo "$line"
        fi
      done < "$OUTPUT" > "$TMPFILE"
      mv "$TMPFILE" "$OUTPUT"
      success "VPNProxy config updated in $OUTPUT"
      ;;
    *)
      echo
      warn "No changes made."
      info "Update $OUTPUT manually if needed, then reload Hammerspoon."
      exit 0
      ;;
  esac
fi

# ── Shell rc files ────────────────────────────────────────────────────────────

header "Verifying Shell Configuration"

add_to_rc() {
  RC_FILE="$1"
  if [ -f "$RC_FILE" ]; then
    if ! grep -q "proxy.env" "$RC_FILE"; then
      echo '[ -f ~/.config/proxy.env ] && . ~/.config/proxy.env' >> "$RC_FILE"
      success "Restored proxy.env sourcing in $RC_FILE"
    else
      success "$RC_FILE already configured"
    fi
  fi
}

add_to_rc "$HOME/.zshrc"
add_to_rc "$HOME/.bashrc"
add_to_rc "$HOME/.bash_profile"

# ── Done ──────────────────────────────────────────────────────────────────────

echo
success "Auto-Detection complete — reload Hammerspoon to apply"
echo
